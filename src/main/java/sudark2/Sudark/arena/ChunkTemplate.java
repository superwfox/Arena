    public void pasteAtChunkData(ChunkGenerator.ChunkData chunkData) {
        int index = 0;
        for (int y = 0; y < height; y++)
            for (int x = 0; x < width; x++)
                for (int z = 0; z < depth; z++)
                    chunkData.setBlock(x, y + baseY, z, blocks[index++]);

        index = 0;
        for (int y = 320; y > 320 - height; y--)
            for (int x = 0; x < width; x++)
                for (int z = 0; z < depth; z++)
                    chunkData.setBlock(x, y + baseY, z, blocks[index++]);
    }

    public int getHeight() {
        return height;
    }

